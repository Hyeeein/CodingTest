SELECT I.ID, N.FISH_NAME, I.LENGTH
FROM FISH_INFO I, FISH_NAME_INFO N
WHERE I.FISH_TYPE = N.FISH_TYPE
     AND (I.FISH_TYPE, I.LENGTH) IN (SELECT FISH_TYPE, MAX(LENGTH) LENGTH 
                                    FROM FISH_INFO 
                                    GROUP BY FISH_TYPE)
ORDER BY I.ID ASC;